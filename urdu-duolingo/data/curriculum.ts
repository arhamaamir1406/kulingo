export const getLessonById = (lessonId: string): { lesson: Lesson; unit: Unit } | null => {
  for (const unit of CURRICULUM) {
    for (const lesson of unit.lessons) {
      if (lesson.id === lessonId) return { lesson, unit };
    }
  }
  return null;
};

export const getNextLesson = (unitId: string, lessonId: string): { lesson: Lesson; unit: Unit } | null => {
  for (let ui = 0; ui < CURRICULUM.length; ui++) {
    const unit = CURRICULUM[ui];
    if (unit.id === unitId) {
      const li = unit.lessons.findIndex((l) => l.id === lessonId);
      if (li !== -1 && li + 1 < unit.lessons.length) {
        return { lesson: unit.lessons[li + 1], unit };
      }
      if (ui + 1 < CURRICULUM.length) {
        return { lesson: CURRICULUM[ui + 1].lessons[0], unit: CURRICULUM[ui + 1] };
      }
    }
  }
  return null;
};
